package com.kodilla.testing.calculator;

public class Calculator {

    int A;
    int B;

    public Calculator(int A, int B){
        this.A = A;
        this.B = B;
    }

    int sum;
    int sub;
    public int add(){
        return sum = A + B;
    }

    public int subtract(){
        return sub = A - B;
    }
}
